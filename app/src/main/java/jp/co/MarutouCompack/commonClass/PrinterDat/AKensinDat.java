package jp.co.MarutouCompack.commonClass.PrinterDat;

import com.google.gson.annotations.SerializedName;

public class AKensinDat {
    @SerializedName("mKSIB")
    private KensinInfoBaseDat mKSIB;
    @SerializedName("mKI")
    private KinInfoDat mKI;
    @SerializedName("mUTC")
    private UTaxCommentDat mUTC;

    public AKensinDat(KensinInfoBaseDat mKSIB, KinInfoDat mKI, UTaxCommentDat mUTC) {
        this.mKSIB = mKSIB;
        this.mKI = mKI;
        this.mUTC = mUTC;
    }

    public KensinInfoBaseDat getKSIB() {
        return mKSIB;
    }

    public void setKSIB(KensinInfoBaseDat mKSIB) {
        this.mKSIB = mKSIB;
    }

    public KinInfoDat getKI() {
        return mKI;
    }

    public void setKI(KinInfoDat mKI) {
        this.mKI = mKI;
    }

    public UTaxCommentDat getUTC() {
        return mUTC;
    }

    public void setUTC(UTaxCommentDat mUTC) {
        this.mUTC = mUTC;
    }
}
