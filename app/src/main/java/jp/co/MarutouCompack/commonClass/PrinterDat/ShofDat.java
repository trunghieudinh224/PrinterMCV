
package jp.co.MarutouCompack.commonClass.PrinterDat;


import com.google.gson.annotations.SerializedName;

/**
 * 商品マスタデータクラス
 */
public class ShofDat {
    /** 使用有無 */
    @SerializedName("mUsef")
    private byte mUsef;
    /** 品目No(取引区分No) */
    @SerializedName("mHinno")
    private short mHinno;
    /** 品番No */
    @SerializedName("mShono")
    private short mShono;
    /** 名称(型式名) */
    @SerializedName("mHinban")
    private String mHinban;
    /** 単位 */
    @SerializedName("mUnit")
    private String mUnit;
    /** 消費税区分 */
    @SerializedName("mTaxKu")
    private byte mTaxKu;
    /** 消費税率 */
    @SerializedName("mTaxR")
    private short mTaxR;
    /** 単価 */
    @SerializedName("mTanka")
    private int mTanka;
    /** 消費税端数処理：加算 */
    @SerializedName("mFracAddTax")
    private int mFracAddTax;
    /** 消費税端数処理：乗算 */
    @SerializedName("mFracAddMult")
    private int mFracAddMult;
    /** 消費税変更日付 */
    @SerializedName("mTax_yy")
    private short mTax_yy;
    /** 消費税変更日付 **/
    @SerializedName("mTax_mm")
    private byte mTax_mm;
    /** 消費税変更日付 */
    @SerializedName("mTax_dd")
    private byte mTax_dd;
    /** 消費税変更旧税率 */
    @SerializedName("mTaxr_old")
    private short mTaxr_old;
    /** 消費税変更新税率 */
    @SerializedName("mTaxr_new")
    private short mTaxr_new;

    public ShofDat(byte mUsef, short mHinno, short mShono, String mHinban, String mUnit, byte mTaxKu, short mTaxR,
                   int mTanka, int mFracAddTax, int mFracAddMult, short mTax_yy, byte mTax_mm, byte mTax_dd,
                   short mTaxr_old, short mTaxr_new) {
        this.mUsef = mUsef;
        this.mHinno = mHinno;
        this.mShono = mShono;
        this.mHinban = mHinban;
        this.mUnit = mUnit;
        this.mTaxKu = mTaxKu;
        this.mTaxR = mTaxR;
        this.mTanka = mTanka;
        this.mFracAddTax = mFracAddTax;
        this.mFracAddMult = mFracAddMult;
        this.mTax_yy = mTax_yy;
        this.mTax_mm = mTax_mm;
        this.mTax_dd = mTax_dd;
        this.mTaxr_old = mTaxr_old;
        this.mTaxr_new = mTaxr_new;
    }

    public byte getUsef() {
        return mUsef;
    }

    public void setUsef(byte mUsef) {
        this.mUsef = mUsef;
    }

    public short getHinno() {
        return mHinno;
    }

    public void setHinno(short mHinno) {
        this.mHinno = mHinno;
    }

    public short getShono() {
        return mShono;
    }

    public void setShono(short mShono) {
        this.mShono = mShono;
    }

    public String getHinban() {
        return mHinban;
    }

    public void setHinban(String mHinban) {
        this.mHinban = mHinban;
    }

    public String getUnit() {
        return mUnit;
    }

    public void setUnit(String mUnit) {
        this.mUnit = mUnit;
    }

    public byte getTaxKu() {
        return mTaxKu;
    }

    public void setTaxKu(byte mTaxKu) {
        this.mTaxKu = mTaxKu;
    }

    public short getTaxR() {
        return mTaxR;
    }

    public void setTaxR(short mTaxR) {
        this.mTaxR = mTaxR;
    }

    public int getTanka() {
        return mTanka;
    }

    public void setTanka(int mTanka) {
        this.mTanka = mTanka;
    }

    public int getFracAddTax() {
        return mFracAddTax;
    }

    public void setFracAddTax(int mFracAddTax) {
        this.mFracAddTax = mFracAddTax;
    }

    public int getFracAddMult() {
        return mFracAddMult;
    }

    public void setFracAddMult(int mFracAddMult) {
        this.mFracAddMult = mFracAddMult;
    }

    public short getTax_yy() {
        return mTax_yy;
    }

    public void setTax_yy(short mTax_yy) {
        this.mTax_yy = mTax_yy;
    }

    public byte getTax_mm() {
        return mTax_mm;
    }

    public void setTax_mm(byte mTax_mm) {
        this.mTax_mm = mTax_mm;
    }

    public byte getTax_dd() {
        return mTax_dd;
    }

    public void setTax_dd(byte mTax_dd) {
        this.mTax_dd = mTax_dd;
    }

    public short getTaxr_old() {
        return mTaxr_old;
    }

    public void setTaxr_old(short mTaxr_old) {
        this.mTaxr_old = mTaxr_old;
    }

    public short getmTaxr_new() {
        return mTaxr_new;
    }

    public void setmTaxr_new(short mTaxr_new) {
        this.mTaxr_new = mTaxr_new;
    }
}
