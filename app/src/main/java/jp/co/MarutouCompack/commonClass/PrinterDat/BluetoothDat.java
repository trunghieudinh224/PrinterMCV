package jp.co.MarutouCompack.commonClass.PrinterDat;

public class BluetoothDat {
    private boolean bBluetoothEnable;
    private boolean bIsConnect;

    public BluetoothDat() {

    }

    public BluetoothDat(boolean bBluetoothEnable, boolean bIsConnect) {
        this.bBluetoothEnable = bBluetoothEnable;
        this.bIsConnect = bIsConnect;
    }

    public boolean isBluetoothEnable() {
        return bBluetoothEnable;
    }

    public void setBluetoothEnable(boolean bBluetoothEnable) {
        this.bBluetoothEnable = bBluetoothEnable;
    }

    public boolean isConnect() {
        return bIsConnect;
    }

    public void setIsConnect(boolean bIsConnect) {
        this.bIsConnect = bIsConnect;
    }
}
