package jp.co.MarutouCompack.commonClass.PrinterDat;

public class ShukeiRowItem {
    /** 項目名 */
    private final String m_strName;
    /** 値 */
    private final String m_strValue;

    /**
     * コンストラクタ.
     *
     * @param strName   [in] String 項目名
     * @param strValue  [in] String 値
     */
    public ShukeiRowItem(String strName, String strValue){
        m_strName = strName;
        m_strValue = strValue;
    }

    /**
     * 項目名の取得.
     *
     * @return  String  項目名
     */
    public String getName(){
        return m_strName;
    }

    /**
     * 値の取得.
     *
     * @return  String  値
     */
    public String getValue(){
        return m_strValue;
    }
}
